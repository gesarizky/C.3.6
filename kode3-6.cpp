#include <iostream>

using namespace std;

int main() {
  // Mendeklarasikan variabel X (diisi nilai 10) 
  // dan Y (diisi nilai 3)
  int X = 10, Y = 3;
  // Mendeklarasikan variabel Z sebagai penampung 
  // nilai hasil operasi
  int Z;

  // Melakukan operasi penjumlahan
  Z = X + Y;

  // Menampilkan hasil penjumahan
  cout<<X<<" + "<<Y<<" = "<<Z;

  return 0;
}
